
#                                         OBJECT ORIENTED PROGRAMMING (OOP)


# In Python, object-oriented Programming (OOP) is a programming paradigm that uses objects and classes in programming. 

# The main concept of OOP is to bind the data and the functions that work on that together as a single unit so that no 

# other part of the code can access this data. Python is an object oriented programming language.

# Almost everything in Python is an object, with its properties and methods.

# A Class is like an object constructor, or a "blueprint" for creating objects.

# In Object Oriented Programming, we're trying to model real-life objects. Those objects have things and can do things.

# The things that they have are called "attributes" (usually modeled with variables), and the things that they can do

# are called "methods" (usually modeled by functions).

# So essentially, an object is just a way of combining some piece of data and some functionality altogether in the same thing.

# A class is a collection of objects. A class contains the blueprints or the prototype from which the objects are being created. 

# It is a logical entity that contains some attributes and methods. 

# For example, a virtual coffee machine is an object, that can be created from a blueprint (known as a Class).

# We can use that Class to create as many virtual coffee machines as we please.

# A method is a function tied to an object. The only difference between "methods" and "functions" is the synthax.

# To call a "method", we first get hold of the object, and then use a dot (.) notation to call the function that is associated

# with the object (also known as a method)



# -- A Class is a blue print for creating new objects

# -- An object is an instance of a class

# -- For example we could have a class called "Human", and objects would be "John", "Mary", "Paul"

# In Python we name functions and variables in lower case, separated by underscores

# But we name Classes using the Pascal naming convention, which requires us to capitalize the first

# letter of every word

# -- To create an object from a class, we simply give the object a name, and set it equal to the name of the class,

#    followed by parentheisis. The parentheisis activates the construction of the object from the blueprint (the Class)

#    Notice that Classes must follow the Pascal naming convention

# Example:                                   object     Class
#                                            ------     -----
#                                            nissan = AutoMobile()

# -- To call a method from an object, we do object.method() e.g. nissan.drive() 

# -- To call an attribute from an object, we do object.attribute e.g. nissan.color

# -- To create a Class, we use the Class keyword, followed by the name of the class, the column, then our block of code (indented)






